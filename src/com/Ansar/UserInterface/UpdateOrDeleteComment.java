    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        UpdateDataInDatabase updateDataInDatabase=new UpdateDataInDatabase();
        try{
        boolean flag=updateDataInDatabase.UpdateINComment(comment.getCommentId(),commenrDetails.getText(),Integer.parseInt(paymentID.getText().replace(" ","")),
                Integer.parseInt(custID.getText().replace(" ","")));
        if(flag)
        {
            Multithreading thread=new Multithreading();
            thread.start();
        }
        }catch(Exception e)
        {
            ImageIcon icon = new ImageIcon("images\\error.png");
            JOptionPane.showMessageDialog(null, e, "Failed", JOptionPane.ERROR_MESSAGE,icon);
        }
    }

    private void editingButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if(editingButton.isSelected())
        {
            paymentID.setEditable(true);
            custID.setEditable(true);
            commenrDetails.setEditable(true);
            updateButton.setVisible(true);
            deleteButton.setVisible(true);
        }
        else
        {
            paymentID.setEditable(false);
            custID.setEditable(false);
            commenrDetails.setEditable(false);
            updateButton.setVisible(false);
            deleteButton.setVisible(false);
        }
    }

    class Multithreading extends Thread{
        public void run() {
            model1.setRowCount(0);
            LinkedList<Comment> commentLinkedList=getDataFromDatabase.getComment();
            for (int count = 0; count < commentLinkedList.size(); count++) {
                model1.addRow(new Object[]{commentLinkedList.get(count).getCommentId(),commentLinkedList.get(count).getPaymentId(),
                        commentLinkedList.get(count).getCustomerId(),commentLinkedList.get(count).getCommentDetails()});
            }
        }
    }

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        DeleteInDatabase deleteInDatabase=new DeleteInDatabase();
        boolean flag=deleteInDatabase.DeleteINComment(comment.getCommentId());
        if(flag)
        {
            Multithreading thread=new Multithreading();
            thread.start();
        }
    }


