    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        GetDataFromDatabase getDataFromDatabase=new GetDataFromDatabase();
        try{
        comment=getDataFromDatabase.getSingleComment(Integer.parseInt(Comment_ID_DetailsText.getText().replace(" ","")));
        if(comment!=null)
        {
            model2.setRowCount(0);
            model2.addRow(new Object[]{comment.getCommentId(),comment.getPaymentId(),comment.getCustomerId(),comment.getCommentDetails()});
            paymentID.setText(String.valueOf(comment.getPaymentId()));
            custID.setText(String.valueOf(comment.getCustomerId()));
            commenrDetails.setText(comment.getCommentDetails());
            editingButton.setVisible(true);
        }
        else
        {
            model2.setRowCount(0);
            paymentID.setText(null);
            custID.setText(null);
            commenrDetails.setText(null);
            editingButton.setVisible(false);
            updateButton.setVisible(false);
            deleteButton.setVisible(false);
        }}catch(Exception e)
        {
            ImageIcon icon = new ImageIcon("images\\error.png");
            JOptionPane.showMessageDialog(null, e, "Failed", JOptionPane.ERROR_MESSAGE,icon);
            model2.setRowCount(0);
            paymentID.setText(null);
            custID.setText(null);
            commenrDetails.setText(null);
            editingButton.setVisible(false);
            updateButton.setVisible(false);
            deleteButton.setVisible(false);
        }
    }

