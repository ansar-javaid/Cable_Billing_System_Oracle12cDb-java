    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        payment=getDataFromDatabase.getSinglePayment(Integer.parseInt(payID.getText().replace(" ","")));
        if(payment!=null)
        {
            model2.setRowCount(0);
            model2.addRow(new Object[]{payment.getPaymentId(),payment.getPaymentDate(),payment.getAmount(),payment.getPaymentMethod(),payment.getCustomerId()});
            paymentMethod.setText(payment.getPaymentMethod());
            custID.setText(String.valueOf(payment.getCustomerId()));
            ammount.setText(String.valueOf(payment.getAmount()));
            paymentDate.setText(payment.getPaymentDate().toString());
            editingButton.setVisible(true);
        }
        else{
            paymentMethod.setText(null);
            custID.setText(null);
            ammount.setText(null);
            paymentDate.setText(null);
            editingButton.setVisible(false);
            updateButton.setVisible(false);
            deleteButton.setVisible(false);
        }
    }

