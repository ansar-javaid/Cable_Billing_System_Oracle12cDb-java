    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {
        DeleteInDatabase deleteInDatabase=new DeleteInDatabase();
        boolean flag=deleteInDatabase.DeleteInPayment(payment.getPaymentId());
        if(flag)
        {
            Multithreading thread=new Multithreading();
            thread.start();
        }
    }

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        UpdateDataInDatabase updateDataInDatabase=new UpdateDataInDatabase();
        boolean flag=updateDataInDatabase.UpdateInPayment(payment.getPaymentId(),updateDataInDatabase.stringToDate(paymentDate.getText()),Double.parseDouble(ammount.getText().replace(" ","")),
                paymentMethod.getText(),Integer.parseInt(custID.getText().replace(" ","")));
        if(flag)
        {
            Multithreading thread=new Multithreading();
            thread.start();
        }
    }

    private void editingButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if(editingButton.isSelected())
        {
            paymentMethod.setEditable(true);
            custID.setEditable(true);
            ammount.setEditable(true);
            paymentDate.setEditable(true);
            updateButton.setVisible(true);
            deleteButton.setVisible(true);
        }
        else
        {
            paymentMethod.setEditable(false);
            custID.setEditable(false);
            ammount.setEditable(false);
            paymentDate.setEditable(false);
            updateButton.setVisible(false);
            deleteButton.setVisible(false);
        }
    }

    class Multithreading extends Thread{
        public void run() {
            model1.setRowCount(0);
            LinkedList<Payment> paymentLinkedList=getDataFromDatabase.getPayment();
            for (int count = 0; count < paymentLinkedList.size(); count++) {
                model1.addRow(new Object[]{paymentLinkedList.get(count).getPaymentId(),paymentLinkedList.get(count).getPaymentDate(),
                        paymentLinkedList.get(count).getAmount(),paymentLinkedList.get(count).getPaymentMethod(),paymentLinkedList.get(count).getCustomerId()});
            }
        }
    }


